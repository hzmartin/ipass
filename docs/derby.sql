CREATE TABLE IPASS
(
ID BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL,
UID BIGINT,
APPUID VARCHAR(256),
KEYWORD VARCHAR(256),
PASSWORD VARCHAR(1024),
REMARK VARCHAR(1024),
CREATE_TIME BIGINT,
UPDATE_TIME BIGINT
);


CREATE UNIQUE INDEX IPASS_UID ON IPASS (UID, APPUID);